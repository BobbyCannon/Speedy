namespace Speedy.Automation.Desktop
{
	/// <summary>
	/// Represents a keyboard key action. Ex. Key Press, Up, Down
	/// </summary>
	public enum KeyboardAction
	{
		/// <summary>
		/// Represents a key press action.
		/// </summary>
		KeyPress,

		/// <summary>
		/// Represents a key down action.
		/// </summary>
		KeyDown,

		/// <summary>
		/// Represents a key up action.
		/// </summary>
		KeyUp
	}
}