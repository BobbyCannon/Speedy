namespace Speedy.Automation.Desktop.Elements
{
	/// <summary>
	/// Specifies whether a window is minimized, maximized, or restored.
	/// </summary>
	public enum WindowState
	{
		/// <summary>
		/// The window is restored.
		/// </summary>
		Normal,

		/// <summary>
		/// The window is minimized.
		/// </summary>
		Minimized,

		/// <summary>
		/// The window is maximized.
		/// </summary>
		Maximized
	}
}